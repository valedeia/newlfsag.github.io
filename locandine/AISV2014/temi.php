<p><strong>Conference Topic: &ldquo;Prosodic and textual aspects of narratives: from oral literature to the speech of the media&rdquo;</strong></p>
  <p>Differences between written and spoken texts are well-known. Laboratory and spontaneous speech show a certain amount of linguistic differences as well as different properties affect the language used by the media. 
In the last decades the attention of linguists and phoneticians focused on these aspects. Many of them were engaged in the search of the most salient features of these distinctions: from macro- and micro-textual characteristics to facts related to rhythm and intonation, including those phenomena which characterise hyperarticulated and hypoarticulated speech. Many phoneticians studied the role played by such indexes as fluency rate, whereas a number of linguists focused on those signals which ensure the cohesion and connexion of the utterances programmed by speakers and their final realisations.
In the 20th century, a reflection started from the observation of the change occurred to communicative conditions following the diffusion of writing, as compared to pure speech, the &ldquo;primary orality&rdquo; not conditioned by this extraordinary means, and led to the consideration of a &ldquo;back orality&rdquo;, the one which has been favoured not only by the new media (radio, telephone, TV), but - around 2000 - by the connectivity and the practicality allowed by new technological tools. 
How the citizens of the third millennium do they speak? How do we tell our stories? Do we speak the same way as illiterate informers used to do when speaking to the interviewers or to the dialectologue of the 30's or do we speak as radio- and TV speakers who progressively entered our home do it today? Do we tell stories as speakers of oral cultures are inclined to do or do we build our speech in a brand new way? Do mnemonic formulae and traditional textual figures still play a role in narratives? How much rhythm and intonation contribute to characterise professional and less conventional speakers?
It is not easy to answer to these questions just using the scientific methods we learned to apply in the framework of a single discipline. It is not easy to do it even when an interdisciplinary approach is available bridging linguistic and engineering together with medical and social sciences: ethnographic and anthropological knowledge are relevant at this purpose. Moreover, it is not possible to investigate these topics without accounting for the influence induced by the personal involvement of the researcher and by neglecting the changes which occurred to the speakers' &ldquo;mentality&rdquo; deriving from their exposition to cultural stimuli always different and nowadays often unpredictable.</p>

