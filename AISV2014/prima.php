  <p>Il prossimo Convegno Nazionale dell&rsquo;Associazione Italiana di Scienze della Voce (AISV) si svolger&agrave; presso la Sala Lauree di via Verdi 10, dal 22 al 24 gennaio 2014.</p>
  <p><strong>Tema del Convegno: &ldquo;Aspetti prosodici e testuali del raccontare: dalla letteratura orale al parlato dei media&rdquo;</strong></p>
  <p>Sono ben note le differenze tra un testo scritto e un testo orale, tra il parlato di laboratorio e il parlato spontaneo e tra questo e il parlato trasmesso. Su queste nei decenni appena trascorsi si è concentrata l'attenzione di linguisti e fonetisti impegnati a catturare tutti i tratti di differenziazione, da quelli strutturali macro- e micro-testuali, a quelli ritmico-intonativi, a quelli che assicurano il passaggio dal parlato iperarticolato a quello ipoarticolato, soffermandosi sul ruolo degli indici di fluenza e sui segnali che garantiscono la coesione e la connessione del programma enunciativo di un parlante e della sua realizzazione finale.
La riflessione parte dal cambiamento delle condizioni comunicative che consegue alla diffusione della scrittura rispetto al parlato puro - all&rsquo;"oralità primaria", non condizionata da questo straordinario mezzo - e si sofferma nel corso del XX secolo su un'"oralità di ritorno", quella favorita non più soltanto dai nuovi media (radio, telefono, TV), ma - alle soglie del 2000 - dalla connettività e dall'operatività consentite dai nuovi mezzi tecnologici. 
Come parlano i cittadini del terzo millennio? Come raccontano le loro storie? Lo fanno allo stesso modo degli informatori analfabeti cui si rivolgeva il dialettologo degli anni &rsquo;30 o come gli speaker radio-televisivi che sono entrati progressivamente nelle nostre case? Raccontano come i parlanti di quelle culture prevalentemente orali oppure costruiscono il loro eloquio in un modo nuovo? Hanno ancora un ruolo mnemotecnico le formule e le figure testuali nella narrativa orale? Quanto gli aspetti ritmico-intonativi contribuiscono a caratterizzare il parlato di speaker e annunciatori meno convenzionali?
A queste domande non è facile dare risposte usando gli strumenti che possiamo imparare a usare con una formazione che, per quanto sempre più interdisciplinare - tra linguistica, medicina, ingegneria -, dimentichi gli aspetti etnografici e antropologici più rilevanti a questo scopo. Allo stesso modo, non è possibile riflettere obiettivamente su questi temi senza lasciarsi influenzare da una posizione di eccessivo coinvolgimento da parte del ricercatore e trascurando i cambiamenti nella "mentalità" dei parlanti derivanti dalla loro esposizione a stimoli culturali diversi e spesso, ormai, imprevedibili.</p>

